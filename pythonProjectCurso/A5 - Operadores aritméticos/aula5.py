"""
OPERADORES ARITMÉTICOS
+ --> adição
- --> subtração
* --> multiplicação
/ --> divisão
// --> divisão inteira
** --> exponenciação
% --> resto da divisão
() --> determina a ordem da operação
"""

print(10 * 10)
print(10 + 10)
print(10 - 5)
print(10 / 2)
print(10.5 // 3)
print(10 ** 2)
print(10 % 3)
print(((5+5)*5)/2)
print(10*'Dani')  # Operação número-string
print('1' + '1')  # Outro exemplo com string
print(20*'A')
print('20'+'A')
print(2 + 5 * 3 ** 2 - (23.5 + 23.5))
