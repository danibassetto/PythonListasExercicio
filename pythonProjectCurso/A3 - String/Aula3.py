'''
str - string = cadeia de caracteres
python sabe que é string quando está entre aspas
TUDO QUE ESTÁ ENTRE ASPAS
'''
print('alguma coisa')
print("Aspas duplas")
print('Essa é uma "string" (str).')
print("Esse é meu \"texto\" (str).")
print('Esse é meu \'texto\' (str).')  # Uso de \ para ele entender que quero usar outras aspas (para imprimi-la)
print(r'Esse é meu texto')  # O r antes significa que nada dentro do print irá ser executado
print("Esse é meu \n(str).") # \n quebra linha
print(123456)  # sem aspas não é uma string, é um n° inteiro