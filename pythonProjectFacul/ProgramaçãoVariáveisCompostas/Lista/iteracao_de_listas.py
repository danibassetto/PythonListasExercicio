# Iterando/percorrendo listas

# COM WHILE
L = [1, 2, 3, 4, 5]
x = 0
while x < len(L):
    print(L[x], end = " ")
    x += 1

A = [10, 20, 30]
b = 0
while b < len(A):
    print(A[b])
    b += 1

# COM FOR

Y = [1, 2, 3, 4, 5]
for i in range(5):
    print(Y[i], end = " ")

W = [1, 2, 3, 4, 5]
for item in W:
    print(item, end =" ")