'''
A iteração tem o objetivo de percorrer os elementos contidos numa lista.

     ITERAÇÃO DE LISTAS COM A INSTRUÇÃO while

'''

L = [1,2,3,4,5]
x = 0
while x < len(L):
    print(L[x],end=" ")
    x+=1

'''
     ITERAÇÃO DE LISTAS COM A INSTRUÇÃO for
'''

L = [1,2,3,4,5]
for item in L:
    print(item,end=" ")
