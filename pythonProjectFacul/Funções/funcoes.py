'''
Funções são especialmente interessantes para isolar uma tarefa específica em um trecho de programa. Isso permite que
a solução de um problema seja reutilizada em outras partes do programa, sem precisar repetir as mesmas linhas.

A função pode ser definida com ou sem parâmetros. Com ou sem retorno.

Uma função deve resolver apenas um problema e, quanto mais genérica for sua solução, melhor ela será em longo prazo.

Para declarar uma função em Python, utiliza-se a instrução def seguida pelo nome da função. Após o nome e entre
parênteses, são definidos os parâmetros que a função receberá. Utiliza-se : após os parâmetros para indicar o início de
um bloco
'''


def ola():
    print("Olá")

ola()
