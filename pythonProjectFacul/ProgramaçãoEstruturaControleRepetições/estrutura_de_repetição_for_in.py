'''
Comando para...
		Sintaxe:

		para <variável> em intervalo (INICIO, FIM):
			<instrução 1>
			<instrução 2>
			...
		<instrução n>

<variável>: cria-se uma variável que irá receber os valores definidos no intervalo;

em: este termo indica que a <variável> irá receber os valores apontados a seguir;

intervalo (INÍCIO, FIM): o laço acontecerá no intervalo definido. A variável irá adquirir os valores definidos do
INÍCIO até o FIM
    Se não for definido o primeiro valor:
    Início é 0:
    para i em intervalo (10):

    O laço irá até o valor antes do fim:

    Exemplo:

    para i em intervalo (0,5):

    O laço acontecerá com os valores:

    0, 1, 2, 3 e 4.
'''